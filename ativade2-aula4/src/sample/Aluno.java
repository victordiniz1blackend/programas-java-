package sample;

public class Aluno {

    private long ra;
    private String nome;
    private double enem;



    public void setRa(long ra) {
        this.ra = ra;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setEnem(double enem) {
        this.enem = enem;
    }

    public long getRa() {
        return ra;
    }

    public String getNome() {
        return nome;
    }

    public double getEnem() {
        return enem;
    }
}
